
public class App{

	public static void main(String[] args) {
		
		int x = 2, y;
		y = 2 * x;
		System.out.println(y);
		
		double z;
		z = 2 * x;
		System.out.println(z);
		
		double height, b, B, h;
		b = 6.0;
		B = 8.0;
		h = 5.0;
		height = ( b + B ) / 2.0 * h;
		System.out.println(height);
		
		int a = 5;
		int c = 2;
		double quotient;
		quotient = (double) a / c;
		System.out.println(quotient);	
		// (double) is an example of 'java casting' = which is an explicit conversion of data types to another
		
	
		
		double l = 5.6;
		int t = (int) l;
		System.out.print(t);
		
		
	}

}
